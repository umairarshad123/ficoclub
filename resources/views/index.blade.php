            phone.classList.add('error');
            document.getElementById('ficoPhoneErr').classList.add('show');
            ok = false;
        }

        if (!document.getElementById('ficoValScore').value) ok = false;
        if (!document.getElementById('ficoValItems').value) ok = false;
        if (!document.getElementById('ficoValGoal').value) ok = false;
        if (!document.getElementById('ficoValTimeline').value) ok = false;

        return ok;
    }

    const SUCCESS_TEXT = "We've received your info. Check your email/text for next steps.";
    const ERROR_TEXT = 'Something went wrong. Please try again.';

    function getCsrf() {
        const meta = document.querySelector('meta[name="csrf-token"]');
        if (meta) return meta.getAttribute('content') || '';
        const hidden = form.querySelector('input[name="_token"]');
        return hidden ? hidden.value : '';
    }

    function firstLaravelError(json) {
        if (json && json.errors) {
            const firstKey = Object.keys(json.errors)[0];
            if (firstKey && json.errors[firstKey] && json.errors[firstKey][0]) {
                return json.errors[firstKey][0];
            }
        }
        return (json && json.message) ? json.message : ERROR_TEXT;
    }

    function swalOk(msg) {
