                title: 'Submitted!',
                text: msg,
                timer: 2400,
                timerProgressBar: true,
                showConfirmButton: true,
                confirmButtonText: 'OK'
            });
        } else {
            alert(msg);
        }
    }

    function swalErr(msg) {
        if (window.Swal) {
            Swal.fire({
                icon: 'error',
                title: 'Submission Failed',
                text: msg,
                confirmButtonText: 'OK'
            });
        } else {
            alert(msg);
        }
    }

    form.addEventListener('submit', async function (e) {
        e.preventDefault();

        if (!validateContact()) {
            swalErr('Please complete all required fields before submitting.');
            return;
        }

        const first = document.getElementById('ficoFirst').value.trim();
        const last  = document.getElementById('ficoLast').value.trim();

        document.getElementById('ficoValFullName').value = [first, last].filter(Boolean).join(' ');
        document.getElementById('ficoPageUrl').value = window.location.href;
        document.getElementById('ficoPageTitle').value = document.title;

