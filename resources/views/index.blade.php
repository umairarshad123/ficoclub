                /* Rewrite as inline style so CSS transition fires */
                needle.style.transform = '';
                needle.style.transition = 'transform 1.4s cubic-bezier(.175,.885,.32,1.1)';
                /* Set via transform attribute after a tick */
                requestAnimationFrame(() => {
                  needle.setAttribute('transform', 'rotate(90,200,200)');
                });
              }, 600);
            }

            /* 3. Count-up score number */
            const display = document.getElementById('meterScoreDisplay');
            const target = 850;
            const duration = 2000;
            const start = Date.now();
            const startVal = 300;
            function countUp() {
              const elapsed = Date.now() - start;
              const progress = Math.min(elapsed / duration, 1);
              const ease = 1 - Math.pow(1 - progress, 4);
              const val = Math.round(startVal + (target - startVal) * ease);
              if (display) display.textContent = val;
              if (progress < 1) requestAnimationFrame(countUp);
              else if (display) {
                display.textContent = target;
                /* Burst glow on finish */
                display.style.textShadow = '0 0 60px rgba(34,197,94,1), 0 0 120px rgba(34,197,94,.5)';
                setTimeout(() => {
