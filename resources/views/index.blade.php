(function() {
  const cards = document.querySelectorAll('.pricing-section .pc');

  // Intersection reveal
  const obs = new IntersectionObserver(entries => {
    entries.forEach(e => {
      if (e.isIntersecting) {
        e.target.classList.add('vis');
        spawnParticles(e.target);
        obs.unobserve(e.target);
      }
    });
  }, { threshold: 0.12 });
  cards.forEach(c => obs.observe(c));

  // Spawn ambient particles
  function spawnParticles(card) {
    for (let i = 0; i < 10; i++) {
      const p = document.createElement('div');
      p.className = 'pt';
      const s = Math.random() * 10 + 4;
      p.style.cssText = `width:${s}px;height:${s}px;left:${Math.random()*100}%;bottom:${Math.random()*40}%;animation-duration:${Math.random()*5+4}s;animation-delay:${Math.random()*5}s;`;
      card.appendChild(p);
    }
  }

  // Magnetic cursor glow
  const glowIds = ['ps-g0','ps-g1','ps-g2'];
  cards.forEach((card, i) => {
    const glow = document.getElementById(glowIds[i]);
    card.addEventListener('mousemove', e => {
      const r = card.getBoundingClientRect();
      const x = e.clientX - r.left;
      const y = e.clientY - r.top;
      glow.style.left = x + 'px';
      glow.style.top = y + 'px';
      const cx = r.width / 2, cy = r.height / 2;
      const dx = (x - cx) / cx;
      const dy = (y - cy) / cy;
      card.style.transform = `translateY(-14px) scale(1.025) rotateX(${-dy * 5}deg) rotateY(${dx * 5}deg)`;
    });
    card.addEventListener('mouseleave', () => {
      card.style.transform = '';
      card.style.transition = 'transform .55s cubic-bezier(.16,1,.3,1), box-shadow .55s cubic-bezier(.16,1,.3,1), filter .55s ease';
    });
    card.addEventListener('mouseenter', () => {
      card.style.transition = 'transform .12s ease-out, box-shadow .55s cubic-bezier(.16,1,.3,1), filter .55s ease';
    });
  });

  // Staggered feature item entrance on card hover
  cards.forEach(card => {
    const items = card.querySelectorAll('.pfeats li');
    card.addEventListener('mouseenter', () => {
      items.forEach((li, i) => {
        li.style.transform = 'translateX(4px)';
        li.style.transitionDelay = (i * 0.04) + 's';
      });
    });
    card.addEventListener('mouseleave', () => {
      items.forEach(li => {
        li.style.transform = 'translateX(0)';
        li.style.transitionDelay = '0s';
      });
    });
  });

  // Ripple on button click
  document.querySelectorAll('.pricing-section .btn').forEach(btn => {
    btn.addEventListener('click', e => {
      const r = document.createElement('span');
