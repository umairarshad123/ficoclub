        document.getElementById('ficoValItems').value = '';
        document.getElementById('ficoValGoal').value = '';
        document.getElementById('ficoValTimeline').value = '';
        document.getElementById('ficoValFullName').value = '';
        document.getElementById('ficoPageUrl').value = window.location.href;
        document.getElementById('ficoPageTitle').value = document.title;

        showPanel(1);
        overlay.classList.add('open');
        document.body.style.overflow = 'hidden';
    }

    function closePopup() {
        overlay.classList.remove('open');
        document.body.style.overflow = '';
    }

    closeBtn.addEventListener('click', closePopup);
    overlay.addEventListener('click', function (e) {
        if (e.target === overlay) closePopup();
    });

    document.addEventListener('keydown', function (e) {
        if (e.key === 'Escape') closePopup();
    });

    buildSteps();
    showPanel(1);

    window.openCreditPopup = openPopup;
    window.closeCreditPopup = closePopup;
})();


