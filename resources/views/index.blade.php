        btn.textContent = 'Submitting...';

        try {
            const response = await fetch(form.action, {
                method: 'POST',
                body: new FormData(form),
                headers: {
                    'X-Requested-With': 'XMLHttpRequest',
                    'X-CSRF-TOKEN': getCsrf(),
                    'Accept': 'application/json'
                }
            });

            const raw = await response.text();
            let data = null;

            try {
                data = JSON.parse(raw);
            } catch (err) {
                data = null;
            }

            if (response.ok) {
                swalOk((data && data.message) ? data.message : SUCCESS_TEXT);
                document.getElementById('ficoPanels').style.display = 'none';
                successEl.classList.add('show');
            } else if (response.status === 422) {
                swalErr(firstLaravelError(data || {}));
            } else {
                swalErr((data && data.message) ? data.message : ERROR_TEXT + ' (HTTP ' + response.status + ')');
            }
        } catch (err) {
            console.error(err);
            swalErr(ERROR_TEXT);
        } finally {
            btn.disabled = false;
            btn.textContent = 'Get My Roadmap 🚀';
        }
    });

    function openPopup() {
        current = 1;
        Object.keys(answers).forEach(k => answers[k] = '');
        document.querySelectorAll('.fico-option').forEach(o => o.classList.remove('selected'));
        document.getElementById('ficoPanels').style.display = 'block';
        successEl.classList.remove('show');
