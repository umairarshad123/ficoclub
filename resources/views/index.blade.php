<script>
(function () {
    'use strict';

    const TOTAL = 5;
    let current = 1;
    const answers = { score: '', items: '', goal: '', timeline: '' };

    const overlay   = document.getElementById('ficoOverlay');
    const modal     = document.getElementById('ficoModal');
    const closeBtn  = document.getElementById('ficoClose');
    const stepsRow  = document.getElementById('ficoStepsRow');
    const panels    = document.querySelectorAll('.fico-panel');
    const successEl = document.getElementById('ficoSuccess');
    const form      = document.getElementById('ficoCreditForm');

    function buildSteps() {
        stepsRow.innerHTML = '';
        for (let i = 1; i <= TOTAL; i++) {
            const item = document.createElement('div');
            item.className = 'fico-step-item';
            item.dataset.stepItem = i;
            item.innerHTML = '<div class="fico-step-circle">' + i + '</div><div class="fico-step-bar"></div>';
            stepsRow.appendChild(item);
        }
    }

    function syncSteps() {
        document.querySelectorAll('.fico-step-item').forEach(el => {
            const n = parseInt(el.dataset.stepItem);
            el.classList.remove('active', 'done');
            if (n === current) el.classList.add('active');
            else if (n < current) el.classList.add('done');
        });
    }

    function showPanel(n) {
        panels.forEach(p => p.style.display = 'none');
        const target = document.querySelector('.fico-panel[data-panel="' + n + '"]');
        if (target) {
            target.style.display = 'block';
            target.style.animation = 'ficoSlideUp .3s ease-out';
        }
        current = n;
        syncSteps();
        modal.scrollTop = 0;
    }

    document.querySelectorAll('.fico-option').forEach(opt => {
        opt.addEventListener('click', function () {
            const name = this.dataset.name;

            document.querySelectorAll('.fico-option[data-name="' + name + '"]').forEach(o => {
                o.classList.remove('selected');
            });

            this.classList.add('selected');
            answers[name] = this.dataset.value;

            const hiddenId = 'ficoVal' + name.charAt(0).toUpperCase() + name.slice(1);
            const hiddenEl = document.getElementById(hiddenId);
            if (hiddenEl) hiddenEl.value = this.dataset.value;

            setTimeout(() => {
                if (current < TOTAL) showPanel(current + 1);
            }, 260);
        });
    });

    document.querySelectorAll('[data-back]').forEach(btn => {
        btn.addEventListener('click', () => {
            if (current > 1) showPanel(current - 1);
        });
    });

    /* ── FIX 1: correct element ID + 11-digit limit with +1 formatting ── */
    document.getElementById('ficoPhone').addEventListener('input', function (e) {
        let v = e.target.value.replace(/\D/g, '');
        if (v.length > 11) { v = v.slice(0, 11); }
        if (v.length > 0) {
            if (v.length <= 1) {
                v = '+' + v;
            } else if (v.length <= 4) {
                v = '+' + v.slice(0, 1) + ' (' + v.slice(1);
            } else if (v.length <= 7) {
                v = '+' + v.slice(0, 1) + ' (' + v.slice(1, 4) + ') ' + v.slice(4);
            } else {
                v = '+' + v.slice(0, 1) + ' (' + v.slice(1, 4) + ') ' + v.slice(4, 7) + '-' + v.slice(7, 11);
            }
        }
        e.target.value = v;
    });

    ['ficoFirst','ficoLast','ficoEmail','ficoPhone'].forEach(id => {
        document.getElementById(id).addEventListener('input', function () {
            this.classList.remove('error');
            const err = document.getElementById(id + 'Err');
            if (err) err.classList.remove('show');
        });
    });

    function validateContact() {
        let ok = true;
        const first = document.getElementById('ficoFirst');
        const last  = document.getElementById('ficoLast');
        const email = document.getElementById('ficoEmail');
        const phone = document.getElementById('ficoPhone');

        [first, last, email, phone].forEach(el => {
            el.classList.remove('error');
            const err = document.getElementById(el.id + 'Err');
            if (err) err.classList.remove('show');
        });

        if (!first.value.trim()) {
            first.classList.add('error');
            document.getElementById('ficoFirstErr').classList.add('show');
            ok = false;
        }

        if (!last.value.trim()) {
            last.classList.add('error');
            document.getElementById('ficoLastErr').classList.add('show');
            ok = false;
        }

        if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email.value.trim())) {
            email.classList.add('error');
            document.getElementById('ficoEmailErr').classList.add('show');
            ok = false;
        }

