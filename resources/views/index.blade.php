        (function() {
          /* ---- Intersection Observer trigger ---- */
          const section = document.querySelector('.score-meter-section');
          if (!section) return;

          const observer = new IntersectionObserver((entries) => {
            entries.forEach(e => {
              if (e.isIntersecting) {
                initMeterAnimation();
                observer.disconnect();
              }
            });
          }, { threshold: 0.3 });
          observer.observe(section);

          function initMeterAnimation() {
            /* 1. Reveal arc segments */
            document.querySelectorAll('.meter-seg').forEach(s => s.style.opacity = '1');

            /* 2. Rotate needle to 850 position (90deg = full right) */
            const needle = document.getElementById('meterNeedle');
            if (needle) {
