        (function(){
          const cardDelayMap = {'d1':160,'d2':280,'d3':400};

          // Inject floating particles into each card
          document.querySelectorAll('.sc-card').forEach(function(card){
            for(var i=0;i<3;i++){
              var p=document.createElement('span');
              p.className='particle';
              card.prepend(p);
            }
          });

          function animateCard(card){
            card.classList.add('visible');
            var cardClass = Array.from(card.classList).find(function(c){ return cardDelayMap[c]; });
            var cardDelay = cardDelayMap[cardClass] || 0;

            var bars = card.querySelectorAll('.bureau-bar');
            var scoreEls = card.querySelectorAll('.b-score-new');

            bars.forEach(function(bar, i){
              var targetW = parseFloat(bar.dataset.to);
              var barDelay = cardDelay + (i * 200);

              bar.style.width = '0%';
              bar.classList.remove('animate','glowing');

              requestAnimationFrame(function(){
                requestAnimationFrame(function(){
                  setTimeout(function(){
                    bar.classList.add('animate');
                    bar.style.width = targetW + '%';

                    // After fill completes → trigger glow
                    setTimeout(function(){
                      bar.classList.add('glowing');
                    }, 1650); // matches transition duration 1.6s + tiny buffer

                  }, barDelay);
                });
              });
            });

            // Count-up numbers staggered to match bars
            scoreEls.forEach(function(el, i){
              var target = parseInt(el.dataset.final);
              var start = Math.round(target * 0.62);
              var dur = 1500;
              var countDelay = cardDelay + (i * 200) + 80;
              setTimeout(function(){
                var t0 = performance.now();
                (function tick(now){
                  var p = Math.min((now - t0) / dur, 1);
                  var e = 1 - Math.pow(2, -10 * p);
                  el.textContent = Math.round(start + (target - start) * e);
                  if(p < 1) requestAnimationFrame(tick);
                })(performance.now());
              }, countDelay);
            });
          }

          var obs = new IntersectionObserver(function(entries){
            entries.forEach(function(entry){
              if(!entry.isIntersecting) return;
              animateCard(entry.target);
              obs.unobserve(entry.target);
            });
          }, {threshold: 0.2});

          document.querySelectorAll('.sc-card').forEach(function(c){ obs.observe(c); });

          var revObs = new IntersectionObserver(function(entries){
            entries.forEach(function(e){ if(e.isIntersecting){ e.target.classList.add('visible'); revObs.unobserve(e.target); }});
          }, {threshold:0.1});
          document.querySelectorAll('.reveal:not(.sc-card)').forEach(function(el){ revObs.observe(el); });
        })();
